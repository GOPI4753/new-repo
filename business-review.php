    <h4>Ford</h4>
    
    <p>Even after being around for such a long time, the EcoSport still is the most handsome looking brute in the sub-four meter SUV segment. Its bold grille, large aggressive headlamps, well-designed fog lamp housing, wraparound C-pillar, muscular creases, and haunches- all lend it a very American muscle-esque persona - Bilal Ahmed</p>
    <P>Fords have been known for their road dynamics and the EcoSport is no different. Its well-balanced steering wheel coupled with well-judged ride quality make this Blue Oval one of the best driver's vehicles in its segment. Take it over any road surface (or no road for that matter) and the ride remains flat and consistent without troubling the passengers. Combine this with the direct and well-weighted steering and the EcoSport emerges as an ideal choice for those who like spending more time behind the wheel - Bilal Ahmed</p>
