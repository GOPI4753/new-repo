    <P>Larger on the inside, now available with a more powerful engine, better-equipped and better-built, the new Wagon R has a lot to offer. It’s fairly well-priced too. Remember that this is not a car to buy if you enjoy driving, and its appliance-like character won’t appeal to enthusiasts either. What the Wagon R does is retain its original formula of an incredibly functional budget car. For spaciousness and sheer practicality, the new Maruti Suzuki Wagon R is still the car to beat - Shapur Kotwal</P>
